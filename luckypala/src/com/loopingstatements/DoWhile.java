package com.loopingstatements;

public class DoWhile {
	public static void main(String[] args) {
		System.out.println("main method started..");

		int a = 5;

		do {
			System.out.println("Hello");
		} while (a > 5);

		do {
			System.out.println("Hello Bhaii !!!");
			a++;
		} while (a < 10);
		
		
	}
}
